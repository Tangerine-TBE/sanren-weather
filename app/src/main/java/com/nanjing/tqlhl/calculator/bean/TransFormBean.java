package com.nanjing.tqlhl.calculator.bean;

public class TransFormBean {
    private int image;
    private String title;

    public TransFormBean(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
